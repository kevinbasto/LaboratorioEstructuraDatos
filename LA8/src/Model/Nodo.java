package Model;

/**
 *
 * @author jevin
 */
public class Nodo {
    
    private int posX;
    private int posY;
    private int id;

    public Nodo(int posX, int posY, int id) {
        this.posX = posX;
        this.posY = posY;
        this.id = id;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
